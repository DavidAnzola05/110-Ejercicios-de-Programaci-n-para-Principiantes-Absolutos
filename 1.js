function ejercicio1_DAAC() {
  let num1_DAAC = 10, operador_DAAC = '+', num2_DAAC = 5;
  let resultado_DAAC;
  if (operador_DAAC === '+') resultado_DAAC = num1_DAAC + num2_DAAC;
  else if (operador_DAAC === '-') resultado_DAAC = num1_DAAC - num2_DAAC;
  else if (operador_DAAC === '*') resultado_DAAC = num1_DAAC * num2_DAAC;
  else if (operador_DAAC === '/') resultado_DAAC = num1_DAAC / num2_DAAC;
  console.log("1️⃣ Resultado calculadora:", resultado_DAAC);
}